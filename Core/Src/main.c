void invert_matrix(float matrix[2][2], float inv_matrix[2][2])
{
	float determinant = matrix[0][0] * matrix[1][1] - matrix[0][1] * matrix[1][0];
    if (determinant == 0)
    {
        return;
    }

    float inv_det = 1.0 / determinant;
    inv_matrix[0][0] =  matrix[1][1] * inv_det;
    inv_matrix[0][1] = -matrix[0][1] * inv_det;
    inv_matrix[1][0] = -matrix[1][0] * inv_det;
    inv_matrix[1][1] =  matrix[0][0] * inv_det;
}

void multiply_matrix_vector(float matrix[2][2], float vector[2], float result[2])
{
    result[0] = matrix[0][0] * vector[0] + matrix[0][1] * vector[1];
    result[1] = matrix[1][0] * vector[0] + matrix[1][1] * vector[1];
}

void Position_Calc(float Time_final, float Position_final, float Time_interval)
{
	float A[2][2] ={
	        {Time_final * Time_final * Time_final, Time_final * Time_final},
	        {3 * Time_final * Time_final, 2 * Time_final}
	    };

	float B[2] = {Position_final, 0};
	float inv_A[2][2];
	float x[2];

	invert_matrix(A, inv_A);
	multiply_matrix_vector(inv_A, B, x);

	float a = x[0];
	float b = x[1];

	PID.Position_Expected = a*Time_interval*Time_interval*Time_interval+b*Time_interval*Time_interval;
}


